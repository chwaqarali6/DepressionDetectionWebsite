$(function() {
    $('SignInButton').click(function() {
        $.ajax({
            url: '/LogIn',
            data: $('form').serialize(),
            type: 'POST',
            success: function(response) {
                console.log(response);
            },
            error: function(error) {
                console.log(error);
            }
        });
    });
});

